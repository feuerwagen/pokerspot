				}//*/
				break;
			case 'update':
				$spot = PokerSpot::getInstance($this->s->element);
				$spot->title = $this->vars['title'];
				$spot->button = $this->vars['button'];
				$spot->stacks = array($this->vars['stack_p1'], $this->vars['stack_p2']);
