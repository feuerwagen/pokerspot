					if (is_array($this->vars['player']) && count($this->vars['player']) > 0) {
						foreach ($this->vars['player'] as $key => $value) {
							if ($this->vars['action'][$key] == 'raise') {
								$params = array('value' => $this->vars['value'][$key], 'rel_value' => ($this->vars['value'][$key] - $raise));
